SELECT
    *
FROM
    USERS
    INNER JOIN COUNTRIES
    ON USERS.COUNTRY_CODE = COUNTRY_CODE
ORDER BY
    CPUNTRY_NAME ASC;

SELECT
    USERS.NAME,
    SUM(TRANSACTION .AMOUNT) AS SUM,
    COUNT(TRANSACTIONS.ID)   AS COUNT
FROM
    USERS
    LEFT JOIN TRANSACTIONS
    ON USERS.ID =TRANSACTION.USER_ID
GROUP BY
    USERS.ID
ORDER BY
    SUM DESC;

